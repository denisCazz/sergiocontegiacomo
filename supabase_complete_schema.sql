-- =============================================
-- PARTE 1: TABELLE PRINCIPALI
-- =============================================

-- Tabella Articles (Blog)
CREATE TABLE IF NOT EXISTS public.articles (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at TIMESTAMPTZ DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
  title TEXT NOT NULL,
  slug TEXT NOT NULL UNIQUE,
  published_at TIMESTAMPTZ DEFAULT TIMEZONE('utc'::text, NOW()),
  author TEXT,
  cover_image TEXT,
  excerpt TEXT,
  content TEXT,
  tags TEXT[]
);

-- Tabella Events
CREATE TABLE IF NOT EXISTS public.events (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at TIMESTAMPTZ DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
  title TEXT NOT NULL,
  slug TEXT NOT NULL UNIQUE,
  date TIMESTAMPTZ,
  time TEXT,
  location TEXT,
  cover_image TEXT,
  pdf_url TEXT,
  description TEXT,
  price TEXT,
  status TEXT CHECK (status IN ('upcoming', 'past')),
  tags TEXT[]
);

-- Tabella Comments (Commenti agli articoli)
CREATE TABLE IF NOT EXISTS public.comments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at TIMESTAMPTZ DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
  article_slug TEXT NOT NULL REFERENCES public.articles(slug) ON DELETE CASCADE,
  user_name TEXT NOT NULL,
  rating INTEGER CHECK (rating >= 1 AND rating <= 5),
  content TEXT NOT NULL
);

-- Tabella Event RSVPs (Iscrizioni eventi)
CREATE TABLE IF NOT EXISTS public.event_rsvps (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at TIMESTAMPTZ DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
  event_slug TEXT NOT NULL REFERENCES public.events(slug) ON DELETE CASCADE,
  user_name TEXT,
  user_email TEXT,
  status TEXT CHECK (status IN ('attending', 'not_attending'))
);

-- Tabella AudioPillole
CREATE TABLE IF NOT EXISTS public.audio_pillole (
  id BIGSERIAL PRIMARY KEY,
  title TEXT NOT NULL,
  description TEXT,
  file_url TEXT NOT NULL,
  duration TEXT,
  published_at DATE NOT NULL DEFAULT CURRENT_DATE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tabella Press (Rassegna Stampa)
CREATE TABLE IF NOT EXISTS public.press (
  id BIGSERIAL PRIMARY KEY,
  title TEXT NOT NULL,
  testata TEXT NOT NULL,
  description TEXT,
  file_url TEXT NOT NULL,
  published_at DATE NOT NULL DEFAULT CURRENT_DATE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

