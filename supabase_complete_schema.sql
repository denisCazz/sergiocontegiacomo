-- =============================================
-- PARTE 3: VIEWS (Statistiche)
-- =============================================

-- View per statistiche articoli
CREATE OR REPLACE VIEW public.article_stats AS
SELECT
  article_slug,
  COUNT(*) AS comment_count,
  AVG(rating)::NUMERIC(3,2) AS average_rating
FROM public.comments
GROUP BY article_slug;

-- View per statistiche eventi
CREATE OR REPLACE VIEW public.event_stats AS
SELECT
  event_slug,
  COUNT(*) FILTER (WHERE status = 'attending') AS attending_count,
  COUNT(*) FILTER (WHERE status = 'not_attending') AS not_attending_count
FROM public.event_rsvps
GROUP BY event_slug;

